<?php
/*Operadores de INCREMENTO E DECREMENTO*/

    $a = 10;
    echo $a++; // incrementa depois (mostra depois)
    echo"<br>";
    echo $a;
    echo "<br>";
    echo "<br>";   

    $b = 20;
    echo ++$b; // incrementa antes (já mostra antes)
    echo "<br>";
    echo $b;
    echo"<br>";
    echo"<br>";

    $c = 30;
    echo $c--; // decrementa depois (mostra depois)
    echo"<br>";
    echo $c;
    echo"<br>";
    echo"<br>";

    $d = 40;
    echo --$d; // decrementa antes (já mostra antes)
    echo"<br>";
    echo $d;

?>